
public class LocalTemperature {
	public double celcius;
    public double farenheit;
    public String City;
    public String State;
	
    public LocalTemperature(double celcius, double farenheit, String city, String state) {
		super();
		this.celcius = celcius;
		this.farenheit = farenheit;
		City = city;
		State = state;
	}
}
