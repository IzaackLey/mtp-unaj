package ejercicioUpdateEspecifico;

import java.util.*;

public class StatisticsDisplay implements WeatherDataObserver, DisplayElement {
	private float maxTemp = 0.0f;
	private float minTemp = 200;
	private float tempSum= 0.0f;
	private int numReadings;
	private WeatherDataSubject weatherDataSubject;

	public StatisticsDisplay(WeatherDataSubject weatherDataSubject) {
		this.weatherDataSubject = weatherDataSubject;
		weatherDataSubject.registerObserver(this);
	}

	public void update(float temp, float humidity, float pressure) {
		tempSum += temp;
		numReadings++;

		if (temp > maxTemp) {
			maxTemp = temp;
		}
 
		if (temp < minTemp) {
			minTemp = temp;
		}

		display();
	}

	public void display() {
		System.out.println("Avg/Max/Min temperature = " + (tempSum / numReadings)
			+ "/" + maxTemp + "/" + minTemp);
	}
}
